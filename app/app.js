function loadData () {
	$.each(CAMP, function(index, item) {
		$("#app").append(`        
		<div id="${index}" class="item-card">
			<img src="images/${item.itemThumbImg}" alt="${item.itemName}">
			<div class="price">${item.itemPrice}</div>
			<div class="item-info">
		  		<h3>${item.itemName}</h3>
		  		<p>${item.itemShortDesc}</p>
			</div>
	  </div>`);
	});

	initListeners();
};

function initListeners () {
	$(".item-card").click(function(e) {
		let itemIndex = e.currentTarget.id;
		console.log(itemIndex)
		$("#app").html(`
		<div class="item-page-container">
        <div class="close">Close</div>
        <div class="item-page">
          <div class="item-name">${CAMP[itemIndex].itemName}</div>
          <div class="portrait-description">
            <div class="full-size-image">
              <img src="images/${CAMP[itemIndex].itemFullImg}" alt="${CAMP[itemIndex].itemName}" />
            </div>
            <div class="description-price">
              <div class="description">${CAMP[itemIndex].itemFullDesc}</div>
              <div class="price">${CAMP[itemIndex].itemPrice}</div>
            </div>
          </div>
        </div>
      </div>`)

	  closeListener();
	});
};

function closeListener () {
	$(".close").click(function (e) {
		$("#app").html("");
		loadData();
	});
};

document.addEventListener('DOMContentLoaded', (event) => {
	console.log('DOM Loaded and Parsed');
	loadData();
});